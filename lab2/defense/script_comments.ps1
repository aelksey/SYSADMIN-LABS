# ------------------------------------ ЧАСТЬ 1 ------------------------------------
# Ознакомление с основами WMI: получение базовой информации о системе

# Получение имени компьютера через WQL-запрос к классу Win32_ComputerSystem
# Win32_ComputerSystem - содержит общую информацию о компьютерной системе (имя, производитель, модель и т.д.)
$computerName = (Get-WmiObject -Query "SELECT Name FROM Win32_ComputerSystem").Name

# Получение списка всех локальных пользователей через WQL-запрос
# Win32_UserAccount - класс, представляющий учетные записи пользователей в системе
# ВНИМАНИЕ: Без фильтрации по Domain может вернуть и доменных пользователей, если ПК в домене
$users = Get-WmiObject -Query "SELECT Name FROM Win32_UserAccount"

# Получение информации о сетевых адаптерах, у которых есть MAC-адрес
# Win32_NetworkAdapter - класс, описывающий сетевые адаптеры (физические и виртуальные)
# Фильтр "WHERE MACAddress IS NOT NULL" отсекает адаптеры без MAC (например, VPN, Loopback)
$networkAdapters = Get-WmiObject -Query "SELECT Name, MACAddress FROM Win32_NetworkAdapter WHERE MACAddress IS NOT NULL"

# Вывод информации в консоль для визуального контроля (отладка)
Write-Host "Computer name:"
Write-Host $computerName
Write-Host ""

Write-Host "List of users:"
# Перебор каждого объекта пользователя и вывод его имени
$users | ForEach-Object {
    Write-Host $_.Name
}
Write-Host ""

Write-Host "Network cards and MAC addresses:"
# Вывод каждого сетевого адаптера в формате "Имя - MAC-адрес"
$networkAdapters | ForEach-Object {
    Write-Host "$($_.Name) - $($_.MACAddress)"
}

# ------------------------------------ ЧАСТЬ 2 ------------------------------------
# Получение информации об автозагрузке (вариант 9)

# Win32_StartupCommand - класс WMI, возвращающий программы, запускаемые при старте системы
# Источники: реестр (Run, RunOnce), папка автозагрузки, групповые политики
# Свойства: Name (имя записи), Command (путь к исполняемому файлу), Location (откуда загружено)
$startupPrograms = Get-WmiObject -Query "SELECT Name, Command, Location FROM Win32_StartupCommand"

# ------------------------------------ ЧАСТЬ 3 ------------------------------------
# Сохранение всех собранных данных в текстовый файл с определённой структурой

# Определение каталога для сохранения отчёта (согласно заданию: C:\SysAdmLab2)
$reportDir = "C:\SysAdmLab2"

# Формирование полного пути к файлу: имя компьютера + расширение .rep
# Join-Path автоматически добавляет разделитель пути (\) между каталогом и именем файла
$reportFile = Join-Path $reportDir "$computerName.rep"

# Подготовка содержимого файла как массива строк
# Используем массив @() для удобного формирования многострочного текста
$fileContent = @(
    # Секция пользователей согласно формату из задания: [users] и список имён
    "[users]"
    # Для каждого пользователя берём только свойство Name
    ($users | ForEach-Object { $_.Name })
    # Пустая строка для визуального разделения секций
    ""
    
    # Секция сетевых адаптеров: [network] и строки "имя, MAC"
    "[network]"
    ($networkAdapters | ForEach-Object { "$($_.Name), $($_.MACAddress)" })
    ""
    
    # Секция автозагрузки (добавлено по заданию части 3)
    # Формат строк: "Name: ..., Command: ..., Location: ..." для наглядности
    "[startup]"
    ($startupPrograms | ForEach-Object { "Name: $($_.Name), Command: $($_.Command), Location: $($_.Location)" })
)

# Сохранение массива строк в файл с кодировкой UTF-8
# Out-File -FilePath - путь к файлу
# -Encoding utf8 - кодировка (задание позволяет выбрать UTF-8 или CP1251)
# Примечание: Если каталог не существует, команда вызовет ошибку!
# В продакшн-скрипте нужно добавить создание каталога: New-Item -ItemType Directory -Force $reportDir
$fileContent | Out-File -FilePath $reportFile -Encoding utf8

# Вывод пути к сохранённому файлу в консоль для подтверждения успешного завершения
Write-Host ""
Write-Host "Data saved to file:"
Write-Host $reportFile

