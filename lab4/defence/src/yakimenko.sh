#!/bin/bash

# ==============================================================================
# НАСТРОЙКА ПЕРЕМЕННЫХ И ПУТЕЙ К ФАЙЛАМ (КОНФИГУРАЦИЯ)
# ==============================================================================

WORK_DIR="/tmp/workers"
RUN_DIR="$WORK_DIR/run"
CONF_FILE="$WORK_DIR/kafedra.conf"
LOG_FILE="$WORK_DIR/yakimenko.log"
OBSERVER_LOG="$WORK_DIR/observer.log" # Файл для отслеживания перехваченных сигналов

# ==============================================================================
# ШАГ 1: ОБРАБОТКА И ПЕРЕХВАТ СИГНАЛОВ (ЗАДАНИЕ 3)
# ==============================================================================

# Функция-обработчик (колбэк), которая вызывается при перехвате сигналов
handle_signal() {
    # $1 — текстовое имя сигнала (например, SIGINT)
    # $2 — стандартный числовой код сигнала в Linux (2 или 3)
    # Записываем строго по заданию: ДАТА ВРЕМЯ КОД_СИГНАЛА и текст отказа
    echo "$(date '+%Y-%m-%d %H:%M:%S') СИГНАЛ_$2 ($1) - Попрошу не беспокоить, работа еще не сделана!" >> "$OBSERVER_LOG"
}

# Встроенная команда trap привязывает функцию к системным сигналам.
# При поступлении SIGINT (Ctrl+C) или SIGQUIT (Ctrl+\) скрипт НЕ завершится, 
# а просто выполнит функцию handle_signal и продолжит работу дальше.
trap 'handle_signal SIGINT 2' SIGINT
trap 'handle_signal SIGQUIT 3' SIGQUIT

# ==============================================================================
# ШАГ 2: ИНИЦИАЛИЗАЦИЯ ОКРУЖЕНИЯ (ЗАДАНИЕ 2)
# ==============================================================================

# Создаем обе директории ОДНОЙ командой mkdir с флагом -p (как требует задание).
# Флаг -p автоматически создает цепочку папок и не выдает ошибку, если они уже есть.
mkdir -p "$WORK_DIR" "$RUN_DIR"

# Вспомогательная функция для стандартизации записей в основной лог-файл
log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" >> "$LOG_FILE"
}

# Защитная проверка: если файла конфигурации нет, продолжать работу бессмысленно
if [ ! -f "$CONF_FILE" ]; then
    log "Критическая ошибка: Файл конфигурации $CONF_FILE не найден."
    exit 1 # Выходим с кодом ошибки
fi

# ==============================================================================
# ШАГ 3: ОБХОД СПИСКА РАБОТНИКОВ ИЗ КОНФИГУРАЦИИ
# ==============================================================================

# Читаем файл конфигурации построчно. Конструкция || [ -n ... ] гарантирует,
# что последняя строка файла будет прочитана, даже если в ее конце нет переноса строки.
while IFS= read -r worker_path || [ -n "$worker_path" ]; do
    
    # Регулярным выражением пропускаем пустые строки или строки-комментарии (начинаются с #)
    [[ -z "$worker_path" || "$worker_path" =~ ^# ]] && continue
    
    # Выделяем чистое имя скрипта-воркера (например, template_worker.sh) из полного пути
    worker_name=$(basename "$worker_path")
    pid=""

    # Ищем процесс через виртуальную файловую систему /proc (требование задания).
    # Перебираем все папки в /proc, имена которых состоят только из цифр (это PID процессов)
    for proc in /proc/[0-9]*; do
        if [ -f "$proc/cmdline" ]; then
            # В файле cmdline аргументы разделены нулевыми байтами (\0).
            # Утилитой tr заменяем их на пробелы, чтобы получить читаемую строку запуска.
            cmd=$(tr '\0' ' ' < "$proc/cmdline" 2>/dev/null)
            current_pid=$(basename "$proc")
            
            # Проверяем: совпадает ли имя воркера И это НЕ PID текущего скрипта ($$)
            if [[ "$cmd" == *"$worker_name"* && "$current_pid" -ne $$ ]]; then
                pid="$current_pid" # Процесс найден, сохраняем его ID
                break
            fi
        fi
    done

    # ==============================================================================
    # ШАГ 4: РЕАКЦИЯ НА СТАТУС ПРОЦЕССА (ПЕРЕЗАПУСК ИЛИ ПРОВЕРКА)
    # ==============================================================================

    if [ -z "$pid" ]; then
        # СИТУАЦИЯ А: Процесс не найден в системе -> требуется перезапуск
        log "Перезапуск: Процесс $worker_name не найден в системе."
        
        # Запускаем скрипт-работник в режиме nohup (игнорировать сигналы рассоединения терминала).
        # >/dev/null 2>&1 перенаправляет весь стандартный вывод и ошибки в "черную дыру".
        # Символ & в конце отправляет выполнение в фоновый режим.
        nohup "$worker_path" >/dev/null 2>&1 &
        
        # Переменная $! хранит PID последнего запущенного в фоне процесса
        log "Успешно запущен $worker_name с новым PID $!"
    else
        # СИТУАЦИЯ Б: Процесс успешно работает -> общаемся с ним через его канал
        log "Анализ: Процесс $worker_name (PID $pid) активен."
        
        # Путь к именованному каналу, который должен был создать сам воркер
        fifo="$RUN_DIR/$pid.pid"
        
        # Проверяем, что файл существует и действительно является именованным каналом (-p)
        if [ -p "$fifo" ]; then
            
            # Открываем файловый дескриптор 4 одновременно на чтение и запись (<>) в этот FIFO.
            # Это предотвращает блокировку нашего скрипта, если воркер временно занят.
            exec 4<>"$fifo"
            
            # Отправляем в дескриптор 4 запрос строго по заданию
            echo "how much?" >&4
            
            # Читаем ответ из дескриптора 4. Флаг -t 2 — это таймаут.
            # Если воркер не ответит за 2 секунды, команда прервется (защита от зависания).
            if read -t 2 total remaining <&4; then
                log "Статус $worker_name: исходное=$total сек., осталось=$remaining сек."
                
                # Вычисляем математический порог: 70% от исходного времени.
                # В Bash нет дробных чисел, поэтому используем целочисленное умножение и деление.
                threshold=$(( total * 70 / 100 ))
                
                # Если оставшееся время строго меньше 70% от начального
                if [ "$remaining" -lt "$threshold" ]; then
                    
                    # Проверка вероятности p=0.3. Вызываем встроенный интерпретатор awk, 
                    # генерируем случайное число rand() от 0 до 1. Если оно < 0.3, возвращаем 1, иначе 0.
                    if [ "$(awk -v p=0.3 'BEGIN{srand(); print (rand() < p) ? 1 : 0}')" -eq 1 ]; then
                        
                        # Вероятность совпала! Отправляем воркеру команду удвоения времени
                        echo "work twice!" >&4
                        
                        # Фиксируем в логе: дата/время, отметка действия, PID ($pid) и имя ($worker_name)
                        log "ОТПРАВЛЕНО 'work twice!' -> Процесс: $worker_name, ID: $pid (Остаток: $remaining из $total)"
                    else
                        log "Условие < 70% выполнено, но вероятность 0.3 не выпала для $worker_name ($pid)."
                    fi
                else
                    log "Процесс $worker_name ($pid) выполнил меньше 30% работы. Коррекция не требуется."
                fi
            else
                log "Ошибка: Не удалось прочитать ответ из канала процесса $worker_name (PID $pid) - Таймаут."
            fi
            
            # Закрываем файловый дескриптор 4, освобождая системные ресурсы
            exec 4>&-
        else
            log "Предупреждение: Файл канала $fifo отсутствует на диске."
        fi
    fi
done < "$CONF_FILE" # Перенаправляем файл конфигурации на вход цикла while
