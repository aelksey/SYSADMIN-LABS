#!/bin/bash

# ============================================================
# Диспетчер (менеджер) процессов для лабораторной работы №4
# Назначение: 
#   - запускать и перезапускать воркеров (worker_*.sh)
#   - опрашивать их через именованные каналы
#   - с вероятностью 0.3 отправлять команду "work twice!"
#   - игнорировать SIGINT и SIGQUIT (сигналы от Ctrl+C и Ctrl+\)
# ============================================================

# ------------------------------------------------------------
# 1. Перехват сигналов (trap)
# ------------------------------------------------------------
# При получении SIGINT (обычно Ctrl+C) или SIGQUIT (Ctrl+\) 
# скрипт НЕ завершается, а пишет сообщение в observer.log и продолжает работу.
# Сигнал игнорируется (т.е. процесс не прерывается).
trap 'echo "$(date "+%Y-%m-%d %H:%M:%S") SIGINT - Попрошу не беспокоить, работа еще не сделана!" >> /tmp/workers/observer.log;' SIGINT
trap 'echo "$(date "+%Y-%m-%d %H:%M:%S") SIGQUIT - Попрошу не беспокоить, работа еще не сделана!" >> /tmp/workers/observer.log;' SIGQUIT

# ------------------------------------------------------------
# 2. Основные директории и файлы
# ------------------------------------------------------------
WORK_DIR="/tmp/workers"               # корневая директория для всех данных
RUN_DIR="$WORK_DIR/run"               # место для именованных каналов (FIFO)
CONF_FILE="$WORK_DIR/kafedra.conf"    # конфигурация: список путей к воркерам
LOG_FILE="$WORK_DIR/yakimenko.log"    # лог работы самого диспетчера

# Создаём директории, если их нет
mkdir -p "$WORK_DIR" "$RUN_DIR"

# ------------------------------------------------------------
# 3. Функция логирования (добавляет временную метку)
# ------------------------------------------------------------
log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" >> "$LOG_FILE"
}

# ------------------------------------------------------------
# 4. Функция поиска PID процесса по имени скрипта (через /proc)
# ------------------------------------------------------------
# Принимает: имя скрипта (например, "worker_1.sh")
# Ищет во всех /proc/[0-9]*/cmdline, где в командной строке (аргументах)
# встречается это имя. Возвращает PID или ничего (пустую строку).
# Такой ручной перебор нужен, потому что задание требует использования /proc,
# а не готовых команд типа pgrep.
find_pid_by_script() {
    local script_name="$1"
    for proc in /proc/[0-9]*; do
        if [ -f "$proc/cmdline" ]; then
            # Читаем cmdline, заменяем нулевые символы (разделители аргументов) пробелами
            cmd=$(tr '\0' ' ' < "$proc/cmdline" 2>/dev/null)
            # Разбиваем на отдельные слова (аргументы) и проверяем каждое
            for word in $cmd; do
                # Если аргумент содержит имя скрипта (например, "./worker_1.sh" или "worker_1.sh")
                if [[ "$word" == *"$script_name" ]]; then
                    basename "$proc"   # выводим PID (число – имя директории в /proc)
                    return 0
                fi
            done
        fi
    done
    return 1   # не найдено
}

# ------------------------------------------------------------
# 5. Проверка наличия конфигурационного файла
# ------------------------------------------------------------
if [ ! -f "$CONF_FILE" ]; then
    log "Критическая ошибка: Файл конфигурации $CONF_FILE не найден."
    exit 1
fi

# Запоминаем время старта скрипта (чтобы выдерживать интервал не менее 59 секунд)
START_TIME=$(date +%s)

# ------------------------------------------------------------
# 6. Основной цикл: читаем конфигурацию построчно
# ------------------------------------------------------------
# В конфиге – полные пути к скриптам воркеров (по одному на строку)
while IFS= read -r worker_path; do
    [ -z "$worker_path" ] && continue          # пропускаем пустые строки
    worker_name=$(basename "$worker_path")     # например, "worker_1.sh"

    # Ищем, запущен ли уже такой процесс
    pid=$(find_pid_by_script "$worker_name")

    if [ -z "$pid" ]; then
        # ------------------------------------------------------------------
        # Случай 1: процесс НЕ запущен → запускаем через nohup
        # ------------------------------------------------------------------
        log "Перезапуск: Процесс $worker_name не найден в системе."
        # nohup – чтобы процесс продолжал работу после выхода из терминала
        # Перенаправляем stdout/stderr в /dev/null, чтобы не мусорить
        nohup "$worker_path" >/dev/null 2>&1 &
        log "Успешно запущен $worker_name с новым PID $!"
    else
        # ------------------------------------------------------------------
        # Случай 2: процесс уже работает → общаемся через каналы
        # ------------------------------------------------------------------
        log "Процесс $worker_name (PID $pid) активен. Отправляем 'how much?'"

        fifo="$RUN_DIR/$pid.pid"               # канал для отправки команд
        reply_fifo="$RUN_DIR/${pid}_reply.pid" # канал для получения ответа

        # Проверяем, существует ли канал команды
        if [ -p "$fifo" ]; then
            # Отправляем запрос "how much?"
            echo "how much?" > "$fifo"

            # Пытаемся прочитать ответ из reply-канала (таймаут 2 секунды)
            if read -t 2 total remaining < "$reply_fifo" 2>/dev/null; then
                log "Ответ от $worker_name: исходное=$total, осталось=$remaining"

                # Вычисляем порог 70% от исходного времени (задание: "менее 70%")
                threshold=$(( total * 70 / 100 ))

                if [ $remaining -lt $threshold ]; then
                    # Оставшееся время меньше 70% → с вероятностью 0.3 (30%) отправляем "work twice!"
                    if [ $(( RANDOM % 10 )) -lt 3 ]; then
                        echo "work twice!" > "$fifo"
                        log "Отправлена команда 'work twice!' процессу $worker_name (PID $pid)"
                    else
                        log "Условие выполнено, но вероятность не сработала."
                    fi
                else
                    log "Оставшееся время ($remaining) >= 70% от исходного ($total)."
                fi
            else
                log "Не удалось получить ответ от $worker_name (таймаут или нет канала ответа)."
            fi
        else
            log "Канал процесса $worker_name ($fifo) не существует."
        fi
    fi
done < "$CONF_FILE"   # передаём файл конфигурации в цикл через перенаправление

# ------------------------------------------------------------
# 7. Обеспечение минимального интервала запуска (для cron)
# ------------------------------------------------------------
# Задание: скрипт должен запускаться cron'ом раз в минуту, но если он
# отработал быстрее 59 секунд – нужно доспать.
END_TIME=$(date +%s)
ELAPSED=$(( END_TIME - START_TIME ))
if [ $ELAPSED -lt 59 ]; then
    SLEEP_TIME=$(( 59 - ELAPSED ))
    log "Скрипт выполнился за $ELAPSED сек. Сон на $SLEEP_TIME сек."
    sleep $SLEEP_TIME
else
    log "Скрипт выполнился за $ELAPSED сек."
fi

# Конец скрипта диспетчера