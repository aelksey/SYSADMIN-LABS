#!/bin/bash

# ============================================================
# Скрипт-воркер для лабораторной работы №4
# Назначение: имитирует долгий процесс, который может получать
# команды через именованный канал и отвечать на них.
# ============================================================

# Получаем имя скрипта без пути (например, worker_1.sh)
SCRIPT_NAME=$(basename "$0")

# Основная директория для хранения логов, каналов и конфигов
WORK_DIR="/tmp/workers"

# Поддиректория для именованных каналов (FIFO)
RUN_DIR="$WORK_DIR/run"

# Файл, куда этот воркер будет писать свой отчёт
REPORT_FILE="$WORK_DIR/report_${SCRIPT_NAME}.log"

# Создаём директорию для каналов, если её нет
mkdir -p "$RUN_DIR"

# Запоминаем PID (идентификатор) этого процесса
PID=$$

# Пишем в отчёт: процесс стартовал
echo "[$PID] $(date '+%Y-%m-%d %H:%M:%S') К работе приступил" >> "$REPORT_FILE"

# ------------------------------------------------------------
# Именованные каналы (FIFO)
# IN_FIFO  – через него диспетчер (yakimenko.sh) будет отправлять команды
# OUT_FIFO – через него воркер будет отправлять ответы
# ------------------------------------------------------------
IN_FIFO="$RUN_DIR/$PID.pid"
OUT_FIFO="$RUN_DIR/${PID}_reply.pid"

# Удаляем старые файлы каналов, если они остались с прошлого запуска
rm -f "$IN_FIFO" "$OUT_FIFO"

# Создаём два именованных канала (pipe) – специальные файлы для межпроцессного общения
mkfifo "$IN_FIFO"
mkfifo "$OUT_FIFO"

# ------------------------------------------------------------
# Параметры работы воркера
# ------------------------------------------------------------
# Генерируем случайное число от 60 до 600 (секунд работы)
TOTAL_TIME=$(( RANDOM % 541 + 60 ))

# Оставшееся время изначально равно полному
REMAINING=$TOTAL_TIME

# Время старта (в секундах от эпохи) – для расчёта реальной длительности
START_TIME=$(date +%s)

# ------------------------------------------------------------
# Функция для удобной записи сообщений в лог-файл отчёта
# ------------------------------------------------------------
log_msg() {
    echo "[$PID] $(date '+%Y-%m-%d %H:%M:%S') $1" >> "$REPORT_FILE"
}

# ------------------------------------------------------------
# Открываем файловые дескрипторы для работы с каналами
# ------------------------------------------------------------
# Дескриптор 3 – для чтения из IN_FIFO (входные команды)
exec 3< "$IN_FIFO"

# Дескриптор 4 – для записи в OUT_FIFO (ответы)
exec 4> "$OUT_FIFO"

# ------------------------------------------------------------
# Основной цикл: работает, пока не истечёт оставшееся время
# ------------------------------------------------------------
while [ $REMAINING -gt 0 ]; do

    # Пытаемся прочитать команду из канала с таймаутом 1 секунда.
    # -t 1 означает "ждать не более 1 секунды".
    # Если за секунду ничего не пришло, read возвращает ненулевой код,
    # и мы просто идём дальше (не залипаем на пустом канале).
    if read -t 1 -r cmd <&3; then
        # Удаляем возможный символ возврата каретки (для совместимости с Windows)
        cmd=$(echo "$cmd" | tr -d '\r')

        # Обрабатываем полученную команду
        case "$cmd" in
            "how much?")
                # Отвечаем диспетчеру: исходное время и оставшееся
                echo "$TOTAL_TIME $REMAINING" >&4
                ;;
            "work twice!")
                # Удваиваем оставшееся время работы
                REMAINING=$(( REMAINING * 2 ))
                # Пишем сообщение в лог-файл
                log_msg "Да когда ж меня отпустят, я уже заполнил контрольные недели!"
                ;;
        esac
    fi

    # Каждую секунду уменьшаем оставшееся время на 1
    REMAINING=$(( REMAINING - 1 ))

    # Ждём реально 1 секунду (чтобы таймер шёл в реальном времени)
    sleep 1
done

# ------------------------------------------------------------
# Завершение работы воркера
# ------------------------------------------------------------
# Закрываем файловые дескрипторы (освобождаем ресурсы)
exec 3<&-
exec 4>&-

# Удаляем файлы именованных каналов, так как они больше не нужны
rm -f "$IN_FIFO" "$OUT_FIFO"

# Расчёт реально отработанного времени (для итогового отчёта)
END_TIME=$(date +%s)
ELAPSED=$(( END_TIME - START_TIME ))
MIN=$(( ELAPSED / 60 ))
SEC=$(( ELAPSED % 60 ))

# Пишем финальное сообщение в лог-файл
log_msg "На сегодня работа завершена, работал $MIN минут и $SEC секунд"