#!/bin/bash

# ==============================================================================
# НАСТРОЙКА ПЕРЕМЕННЫХ И ПУТЕЙ К ФАЙЛАМ
# ==============================================================================

# Получаем имя текущего файла (например, template_worker.sh) без полного пути к нему
SCRIPT_NAME=$(basename "$0")

# Базовая папка для работы скриптов по заданию
WORK_DIR="/tmp/workers"

# Папка, где будут лежать именованные каналы (файлы управления процессами)
RUN_DIR="$WORK_DIR/run"

# Путь к файлу отчета. Имя лога строго соответствует имени запущенного скрипта
REPORT_FILE="$WORK_DIR/report_${SCRIPT_NAME}.log"

# Создаем папку для каналов, если её еще нет (-p предотвращает ошибку, если папка существует)
mkdir -p "$RUN_DIR"

# ==============================================================================
# ШАГ 1: СТАРТ И ФИКСАЦИЯ ИНИЦИАЛИЗАЦИИ
# ==============================================================================

# Переменная $$ хранит PID (ID процесса) текущего запущенного скрипта
PID=$$

# Записываем первую строчку в лог: [PID] ТЕКУЩАЯ_ДАТА_И_ВРЕМЯ К работе приступил
# Символ >> означает "дописать в конец файла", сохраняя старые записи
echo "[$PID] $(date '+%Y-%m-%d %H:%M:%S') К работе приступил" >> "$REPORT_FILE"

# ==============================================================================
# ШАГ 2: СОЗДАНИЕ ИМЕНОВАННОГО КАНАЛА (FIFO)
# ==============================================================================

# Формируем путь к именованному каналу. Имя файла — это PID нашего процесса
FIFO="$RUN_DIR/$PID.pid"

# На всякий случай удаляем старый файл с таким именем, если он вдруг остался в системе
rm -f "$FIFO"

# Команда mkfifo создает специальный файл типа FIFO (именованный канал) для межпроцессного взаимодействия
mkfifo "$FIFO"

# ==============================================================================
# ШАГ 3: ИНИЦИАЛИЗАЦИЯ ТАЙМЕРОВ И СЛУЧАЙНЫХ ЧИСЕЛ
# ==============================================================================

# Вычисляем случайное время работы от 60 до 600 секунд.
# RANDOM генерирует число от 0 до 32767. Ограничиваем диапазон с помощью остатка от деления (%)
TOTAL_TIME=$(( RANDOM % 541 + 60 )) 

# Переменная для динамического отсчета. Изначально она равна общему сгенерированному времени
REMAINING=$TOTAL_TIME

# Запоминаем реальное время старта в формате Unix Timestamp (количество секунд с 1 января 1970 года)
START_TIME=$(date +%s) 

# Функция-помощник для записи сообщений в лог-файл (чтобы не писать длинный echo каждый раз)
log_msg() {
    echo "[$PID] $(date '+%Y-%m-%d %H:%M:%S') $1" >> "$REPORT_FILE"
}

# ==============================================================================
# ШАГ 4: НАСТРОЙКА И ЗАПУСК ФОНОВОГО ЧИТАТЕЛЯ КОМАНД
# ==============================================================================

# Важный трюк: открываем файловый дескриптор номер 3 одновременно на чтение и запись (<>) в FIFO.
# Это нужно, чтобы канал не закрывался и не блокировал скрипт, когда в него никто ничего не пишет.
exec 3<> "$FIFO"

# Запускаем бесконечный цикл, который будет выполняться параллельно (в фоновом режиме)
while true; do
    # Читаем строку из файлового дескриптора 3 в переменную cmd
    if read -r cmd <&3; then
        # Очищаем строку от символа возврата каретки (\r), если команда отправлена из Windows-системы
        cmd=$(echo "$cmd" | tr -d '\r')
        
        # Конструкция case проверяет, какая именно текстовая команда пришла в канал
        case "$cmd" in
            "how much?")
                # Ответ отправляем обратно в тот же канал (в дескриптор 3).
                # Символ & в конце отправляет эту операцию в фон, чтобы скрипт сам себя не заблокировал
                echo "$TOTAL_TIME $REMAINING" >&3 &
                ;;
            "work twice!")
                # Увеличиваем оставшееся время работы в 2 раза
                REMAINING=$(( REMAINING * 2 ))
                # Вызываем функцию логирования и пишем фразу по заданию
                log_msg "Да когда ж меня отпустят, я уже заполнил контрольные недели!"
                ;;
        esac
    fi
done & # Символ & в конце цикла while запускает весь этот блок в фоновом подпроцессе

# Запоминаем PID фонового процесса-читателя, чтобы позже его корректно закрыть
READER_PID=$!

# ==============================================================================
# ШАГ 5: ОСНОВНОЙ ЦИКЛ ОЖИДАНИЯ (ТАЙМЕР)
# ==============================================================================

# Цикл работает до тех пор, пока оставшееся время строго больше нуля.
# Если придет команда "work twice!", переменная REMAINING увеличится, и цикл автоматически продлится
while [ $REMAINING -gt 0 ]; do
    sleep 1 # Засыпаем ровно на 1 секунду
    REMAINING=$(( REMAINING - 1 )) # Уменьшаем счетчик оставшихся секунд на 1
done

# ==============================================================================
# ШАГ 6: ОЧИСТКА РЕСУРСОВ И ОСТАНОВКА ПРОЦЕССОВ
# ==============================================================================

# Принудительно завершаем фоновый процесс чтения из канала, так как основное время вышло
kill $READER_PID 2>/dev/null

# Закрываем ранее созданный файловый дескриптор номер 3
exec 3>&-

# Удаляем файл именованного канала с диска, как требует задание
rm -f "$FIFO"

# ==============================================================================
# ШАГ 7: ПОДСЧЕТ ИТОГОВ И ЗАВЕРШЕНИЕ РАБОТЫ
# ==============================================================================

# Фиксируем время завершения скрипта в секундах Unix
END_TIME=$(date +%s)

# Вычисляем, сколько секунд скрипт РЕАЛЬНО отработал (с учетом всех возможных удвоений времени)
ELAPSED=$(( END_TIME - START_TIME ))

# Переводим чистые секунды в минуты и остаток секунд для красивого вывода
MIN=$(( ELAPSED / 60 ))
SEC=$(( ELAPSED % 60 ))

# Пишем финальное сообщение в лог-файл со временем фактической работы
log_msg "На сегодня работа завершена, работал $MIN минут и $SEC секунд"
