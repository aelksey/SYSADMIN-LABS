#!/bin/bash
# Скрипт для сбора информации о системе через SNMP (вариант 9)
# Информация о SWAP и дисковых устройствах

# Определяем переменные для удобства (можно заменить на ваши значения)
COMMUNITY="commavt-341-9"  # community name для доступа к SNMP
HOST="127.0.0.1"           # локальный хост (можно заменить на IP сервера)
OUTPUT="/root/avt341-9.log" # файл для сохранения результата

# ============================================================================
# Часть 1: SWAP Section - получение информации о файле подкачки
# ============================================================================

# 1.1 Получение общего размера SWAP (Total Swap)
# OID .1.3.6.1.4.1.2021.4.3.0 - memTotalSwap (общий объем swap в килобайтах)
snmpwalk -v 2c -c $COMMUNITY $HOST .1.3.6.1.4.1.2021.4.3.0 | \
awk -F'[.:: ]+' '{
    # Разделитель полей: [.:: ]+ - один или более символов из набора: точка, двоеточие, пробел
    # Это разбивает строку "UCD-SNMP-MIB::memTotalSwap.0 = INTEGER: 2097152 kB" на поля:
    # $1="UCD-SNMP-MIB", $2="memTotalSwap", $3="0", $4="=", $5="INTEGER", $6="2097152", $7="kB"
    
    print $2 ": " $6, $7   # Выводим: "memTotalSwap: 2097152 kB"
}' >> $OUTPUT

# 1.2 Получение доступного SWAP (Available Swap)
# OID .1.3.6.1.4.1.2021.4.4.0 - memAvailSwap (доступный объем swap в килобайтах)
snmpwalk -v 2c -c $COMMUNITY $HOST .1.3.6.1.4.1.2021.4.4.0 | \
awk -F'[.:: ]+' '{
    # Аналогично предыдущему, разбиваем строку на поля
    # Выводим: "memAvailSwap: 2097152 kB"
    print $2 ": " $6, $7
}' >> $OUTPUT

# 1.3 Вычисление использованного SWAP (Used Swap)
# Получаем всю ветку памяти (ветка .1.3.6.1.4.1.2021.4 содержит все параметры памяти)
snmpwalk -v 2c -c $COMMUNITY $HOST .1.3.6.1.4.1.2021.4 | \
awk '
# Шаблон для поиска строки с общим объемом swap
/memTotalSwap.0/ {
    total = $4   # 4-е поле содержит числовое значение (2097152)
    # Пример строки: "UCD-SNMP-MIB::memTotalSwap.0 = INTEGER: 2097152"
    # Поля по пробелам: $1="UCD-SNMP-MIB::memTotalSwap.0", $2="=", $3="INTEGER:", $4="2097152"
}

# Шаблон для поиска строки с доступным swap
/memAvailSwap.0/ {
    avail = $4   # 4-е поле содержит числовое значение
}

# Блок END выполняется после обработки всех строк
END {
    # Вычисляем использованный swap как разницу между общим и доступным
    # awk автоматически преобразует строки в числа при арифметической операции
    print "Swap Used:", total - avail, "kB"
}' >> $OUTPUT

# Добавляем пустую строку для разделения секций
echo "" >> $OUTPUT

# ============================================================================
# Часть 2: Disks Section - получение информации о дисковых устройствах
# ============================================================================

# Получаем всю ветку дисков (ветка .1.3.6.1.4.1.2021.9.1 содержит dskTable)
snmpwalk -v2c -c $COMMUNITY $HOST .1.3.6.1.4.1.2021.9.1 | \
awk -F' = ' '
# BEGIN блок выполняется один раз перед обработкой строк
BEGIN {
    # Выводим заголовки таблицы
    # Формат: %-25s - строка шириной 25 символов с выравниванием влево (-)
    # | - вертикальная черта для разделения столбцов
    # \n - символ новой строки
    
    printf "%-25s | %-30s | %-12s | %-12s | %-12s | %-12s | %-12s\n", \
           "dskPath", "dskDevice", "dskTotal(kB)", "dskUsed(kB)", \
           "dskAvail(kB)", "dskPercent(%)", "dskPercentNode(%)"
    
    # Выводим разделительную линию (повторяем символы для каждого столбца)
    printf "%-25s | %-30s | %-12s | %-12s | %-12s | %-12s | %-12s\n", \
           "-------------------------", "------------------------------", \
           "------------", "------------", "------------", "-------------", "-------------"
}

# Основной блок выполняется для каждой строки от snmpwalk
{
    # ===== Очистка имени параметра (первое поле) =====
    # Пример исходной строки: "UCD-SNMP-MIB::dskPath.1 = STRING: "/""
    # После разделения по " = ": $1="UCD-SNMP-MIB::dskPath.1", $2="STRING: "/""
    
    # Удаляем все символы до последних двух двоеточий :: в первом поле
    # /^.*::/ - регулярное выражение:
    #   ^ - начало строки
    #   .* - любое количество любых символов
    #   :: - два двоеточия
    gsub(/^.*::/, "", $1)
    # Результат: "dskPath.1"
    
    # Удаляем суффикс .0 в конце (для скалярных значений, но у нас индексы могут быть >0)
    # /\.0$/ - точка, ноль, конец строки
    gsub(/\.0$/, "", $1)
    # Результат остается "dskPath.1" (т.к. нет .0 на конце)
    
    # ===== Извлечение индекса диска =====
    # Разбиваем строку по точке на части
    # split() возвращает количество частей, результат записывается в массив parts
    split($1, parts, ".")
    # parts[1] = "dskPath" (имя параметра)
    # parts[2] = "1" (индекс диска)
    param = parts[1]   # имя параметра
    idx = parts[2]     # индекс диска (1, 2, 3...)
    
    # ===== Очистка значения (второе поле) =====
    value = $2         # исходное значение: "STRING: "/"" или "INTEGER: 12345"
    
    # Удаляем префикс "STRING: " если есть
    # /^STRING: / - начало строки, слово STRING:, пробел
    gsub(/^STRING: /, "", value)
    # Удаляем префикс "INTEGER: " если есть
    # /^INTEGER: / - начало строки, слово INTEGER:, пробел
    gsub(/^INTEGER: /, "", value)
    # Удаляем открывающую кавычку в начале (если есть)
    # /^"/ - начало строки, кавычка
    gsub(/^"/, "", value)
    # Удаляем закрывающую кавычку в конце (если есть)
    # /"$/ - кавычка, конец строки
    gsub(/"$/, "", value)
    
    # Пример очистки:
    # "STRING: "/"" -> после 1: "/"" -> после 3: /"" -> после 4: /
    # "INTEGER: 12345" -> после 2: 12345
    
    # ===== Сохранение значений в ассоциативные массивы =====
    # Ключом массива является индекс диска (idx)
    # Это позволяет собрать все параметры для каждого диска в одном месте
    
    # dskPath - точка монтирования (например, "/", "/boot")
    if (param == "dskPath") path[idx] = value
    
    # dskDevice - устройство (например, "/dev/sda1", "/dev/mapper/root")
    if (param == "dskDevice") device[idx] = value
    
    # dskTotal - общий размер в килобайтах
    if (param == "dskTotal") total[idx] = value
    
    # dskUsed - использованное пространство в килобайтах
    if (param == "dskUsed") used[idx] = value
    
    # dskAvail - доступное пространство в килобайтах
    if (param == "dskAvail") avail[idx] = value
    
    # dskPercent - процент использованного пространства
    if (param == "dskPercent") percent[idx] = value
    
    # dskPercentNode - процент использованных inode
    if (param == "dskPercentNode") inode[idx] = value
}

# END блок выполняется один раз после обработки всех строк
END {
    # Перебираем все ключи (индексы) в массиве path
    # for (i in path) - цикл по всем существующим индексам дисков
    for (i in path) {
        # Выводим строку таблицы для каждого диска
        # %-25s | %-30s | ... - форматированный вывод с фиксированной шириной столбцов
        printf "%-25s | %-30s | %-12s | %-12s | %-12s | %-13s | %-12s\n", \
               path[i], device[i], total[i], used[i], avail[i], percent[i], inode[i]
    }
}' >> $OUTPUT

# Выводим сообщение об успешном завершении
echo "Результат сохранен в $OUTPUT"